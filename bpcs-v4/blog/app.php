<center><h1 style="padding-top:20px; color:white;"> </h1> <h4 style="padding-bottom:8px;">Do you need an app for that? - January 2015</h4></center>
                          
	
				
					
							
<p style="margin-top:10px; padding:5px"><i>..your scientists were so preoccupied with whether or not they could that they didn't stop to think if they should.</i> — Dr. Ian Malcolm (Jurassic Park)</p>

<p style="margin-top:10px; padding:5px"><b><u>Humble Beginnings</u></b></p>

<p style="margin-top:10px; padding:5px">
Recently, I was in the good company of a group of friends and acquaintances, enjoying some food, wine, and cigars. Towards the end of the evening, conversations shifted from sports, current events, religion and politics to business and technology by a couple of guys who were co-founders of a winery startup.</p>

<p style="margin-top:10px; padding:5px">
I absolutely love talking with passionate entrepreneurs. These two particular co-founders were wine aficionados and fervent believers in the power of wine to build and foster personal relationships. I was surrounded by the fruits of their labor: oak wine barrels filled with aging reds and well-organized micro-oxygenation equipment, all contained within a garage. It was an impressive labor of love.</p> 

<p style="margin-top:10px; padding:5px">
As we sat around, reviewing various digital artifacts, such as the winery’s logo and concepts for the wine bottle labels, the question was posed:</p>

<p style="margin-top:10px; padding:5px">“What do you think about making a mobile app for the winery?”</p>

<p style="margin-top:10px; padding:5px"><b><u>Do people actually use apps?</u></b></p>

<p style="margin-top:10px; padding:5px">
Infographics (like the one below) really get people excited about the idea of creating a mobile app:</p>
<br>


 <!-- image -->

<figure><img style="padding-left:5px; padding-right:8px; padding-bottom: 8px; float:left;" height="100%" width="100%" src="images/graph.jpg" alt="blueprint consulting services"></figure>


<p style="margin-top:10px; padding:5px"><i>Infographic provided by Statista</i></p>

<p style="margin-top:10px; padding:5px">
179 million app downloads in 2014, with a projected 49% increase by 2017 is enough to spark the idea of creating a mobile app for almost any reason, especially when mobile app usage in 2014 increased by 86%.</p>

<p style="margin-top:10px; padding:5px">
Another interesting statistic is provided by Nielsen:</p>
<br>



 <!-- image -->

<figure><img style="padding-left:5px; padding-right:8px; padding-bottom:20px; float:left;" height="100%" width="100%" src="images/graph2.png" alt="blueprint consulting services"></figure>


<p style="margin-top:10px; padding:5px">
Not only is the number of apps downloaded increasing, but the time people spend using mobile apps is increasing as well. But, if you look at the numbers a bit more, there is another trend: While app usage is on the rise, the average number of apps used is relatively unchanged.</p>

<p style="margin-top:10px; padding:5px">
Yes, people use apps. However, your potential app is competing with the other 25 (on average) apps people use.</p>

<p style="margin-top:10px; padding:5px">
<b>To stand out, your potential app has to be great.</b></p>

<p style="margin-top:10px; padding:5px"><b><u>What makes a great app?</u></b></p>

<p style="margin-top:10px; padding:5px">
As a success metric, the word “great” can be highly subjective.</p>

<p style="margin-top:10px; padding:5px">
To management, a great app may be one that has 12,000 monthly downloads, 5 million monthly pageviews, and $20k in monthly advertising revenues.</p>

<p style="margin-top:10px; padding:5px">
To a developer, a great app may be an AngularJS web app, that uses Node.js, MongoDB and socket.io for delivering real-time sports information from a third party API, with a performance improvement of 62.7 seconds per request over the previous iteration.</p>

<p style="margin-top:10px; padding:5px">
However, to the manager, developer and designer (roles I have held in various organizations throughout my career), I would argue that these are not descriptions of a great app.</p>

<p style="margin-top:10px; padding:5px">
Revenue is the result of a valid business model. Development libraries, frameworks and technology selections are the result of a thorough development analysis. Performance improvement is the response to internal testing and user feedback. Visual designs are the result of usability tests and/or stakeholder input. However, by themselves, they are merely facets of a great app.</p>

<p style="margin-top:10px; padding:5px">
<b>A great app is one that its core audience loves to use, regardless of competition or trend.</b></p>

<p style="margin-top:10px; padding:5px"><b><u>Who is your core audience?</u></b></p>

<p style="margin-top:10px; padding:5px">
The terms “core audience” and “target market” are often, yet inappropriately, used interchangeably.</p>

<p style="margin-top:10px; padding:5px">
Philip Kotler, a well known and respected marketing author, consultant and professor is often quoted for defining target market as <i>“a well-defined set of customers whose needs the organization plans to satisfy”.</i> Marketers will often structure this set of customers based upon demographics such as age, gender, occupation, education level, marital status, income, and location. Other management types may talk about target markets in terms of segment attractiveness, and whether that segment will fit into a company’s overall business objectives, available resources and capabilities.</p>

<p style="margin-top:10px; padding:5px">
Focusing on the target market is how an app will be monetized, but does not guarantee a great app. In 2008, an iPhone app named “iFart Mobile” reached the #1 spot on the Top 25 Top Paid chart within the Apple App Store and was earning an estimated $10,000 a day. The developer, Joel Comm, understood his target market and profited accordingly, releasing a simple and funny novelty app that people were willing to pay 99 cents for.</p>

<p style="margin-top:10px; padding:5px">
Is iFart Mobile a great app? Despite it’s early financial success with its target market, it’s hard to determine who the core audience is. A search on the Apple App store returns over 1,000 apps with similar functionality. Overall, the app has an average rating with a mix bag of reviews ranging from “OMG so funny” to “Bad App”.</p>

<p style="margin-top:10px; padding:5px">
An app’s core audience will evangelize the app to their family, friends and any stranger who is willing to listen. They will demonstrate, illustrate and pontificate all the ways the app has changed their lives. An app’s core audience will find other uses for the app, often pushing the use case beyond the original intent. They do all of this, not because of clever marketing, pricing or popularity, but because the app addressed a very specific pain in the core audience’s life in an innovative manner.</p>

<p style="margin-top:10px; padding:5px">
<b>An app’s core audience is defined by a very particular problem.</b></p>

<p style="margin-top:10px; padding:5px"><b><u>What problem does your app address?</u></b></p>

<p style="margin-top:10px; padding:5px">
<i>You have to be burning with an idea, or a problem, or a wrong that you want to right.</i> — Steve Jobs</p>

<p style="margin-top:10px; padding:5px">
The best problem to address is one you have direct experience with right now. This sounds easy, but hubris may cloud a potential product owner’s judgement; they are more in love with being a product owner than solving an actual problem.</p>

<p style="margin-top:10px; padding:5px">
Some product owners will define the problem as “We don't have an app”. As a result, they often end up with a “Me too” app with low usage, lackluster adoption rate and will accept the “It’s good enough” mentality when it comes to measuring the app’s success.</p>

<p style="margin-top:10px; padding:5px">
Other product owners will confuse a problem with an observation. They will evaluate perceived “holes” in a particular market, making statements such as “Over X number of people perform action Y, yet no app exists that allows them to do Z”. The lack of an app doesn't necessarily mean a problem exists.</p>

<p style="margin-top:10px; padding:5px">
Then, there are those product owners that lack a solid understanding of how their core audience uses technology, yet, forge a problem that has no real pain point. You may hear statements such as “Our content gets X number of page views from users on a mobile device, so we should build an app that displays our content natively. This will be a better user experience”. This may sound similar to the product owner that wants a “Me too” app, but their motivations are very different. However, neither have defined a real problem.</p>

<p style="margin-top:10px; padding:5px"><b><u>The logical existence of an app defined</u></b></p>

<p style="margin-top:10px; padding:5px">
Based upon our conclusions, we can apply the following deductive reasoning:</p>

<p style="margin-top:10px; padding:5px">
A core audience is defined by a real problem.
<br>An app cannot exist without a core audience.
<br>Therefore, an app cannot be defined without a real problem.</p>

<p style="margin-top:10px; padding:5px">
Considering this logic, the true question a potential product owner should ask is not “Do you need an app for that” but rather, “Do you have a real problem you wish to solve”?</p>

<p style="margin-top:10px; padding:5px"><b><u>What about the winery?</u></b></p>

<p style="margin-top:10px; padding:5px">
One final question remains: What do I think about making a mobile app for the winery?</p>

<p style="margin-top:10px; padding:5px">
When first posed the question, I carried on for a good 10 minutes without directly answering the question. Instead, I opted to provoke the co-founders to think about the need for a mobile app differently. After said provocation, one of the cofounders noted, “Perhaps the app transcends the winery”.</p>

<p style="margin-top:10px; padding:5px">
Exactly.</p>

<p style="margin-top:10px; padding:5px">
Framing the question around my own experiences, I want an app that helps me make sense of purchasing local wines. I purchase wines based upon my limited perception in how they will pair with the food I intend to eat. I understand the basics; red wines should be had with red meat and white wines with white meat. I have books specific to wine appreciation on my bookshelf. I have even gleaned some specific bits of knowledge from wine makers over the years, such as pairing a good syrah with steak due in part to the peppery note in the finish.</p>

<p style="margin-top:10px; padding:5px">
I'm new to the area and know just enough about wine to be dangerous. I’m intrigued by labels, listen to recommendations and appreciate the passion shared by local wine drinkers. My pain point: what local wines should I purchase?</p>

<p style="margin-top:10px; padding:5px">
Give me an app that recommends wine pairings based upon the simple question “What are you eating tonight”. I want the recommended wines to come from a local winery, with reviews provided by residents and local app users. Give me tips about the best way to store, open and serve the wine. Give me a breakdown of the best local places to purchase wines. Give me recipes for food dishes that, when paired with the recommended wines, bring out the best flavors in both the food and drink. In short, aid me in my aspirations to be a man that truly understands how to enjoy good food, wine and company.</p>

<p style="margin-top:10px; padding:5px">
Do those things, and I will use your app constantly. Not because I have to, but because I love to use great apps.</p>


<p style="margin-top:10px; padding:5px"><b>- Gary Nakanelua, Developer
<br>Blueprint Consulting Services</b></p>



							
